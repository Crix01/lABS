a = int(input())  # считывает число от пользователя и преобразует его в целое число, сохраняя в переменную a
import math  # импорт math
import datetime  # импорт datetime
b = math.sqrt(a)  # вычисляет квадратный корень из числа a с помощью функции sqrt() из модуля math и сохраняет результат в переменную b
с = datetime.datetime.now()  # получает текущую дату и время с помощью функции now() из модуля datetime и сохраняет результат в переменную с
print(b)  # вывод
print(с)  # вывод